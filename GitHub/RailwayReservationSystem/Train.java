public class Train{
    String name;
    String time;
    int passengerStrength;
    int trainNumber;

    Train(String name, String time, int passengerStrength, int trainNumber) {
        this.name = name;
        this.time = time;
        this.passengerStrength = passengerStrength;
        this.trainNumber = trainNumber;
    }
}
